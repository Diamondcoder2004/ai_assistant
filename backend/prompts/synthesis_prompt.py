# =============================================================================
# SELECTOR — выбор промпта по режиму
# =============================================================================

def get_synthesis_prompt(mode: str = "standard") -> str:
    """
    Возвращает промпт для синтеза ответа в зависимости от режима.

    Args:
        mode: "brief" — кратко, "standard" — стандартно, "detailed" — подробно

    Returns:
        Текст промпта
    """
    prompts = {
        "brief": BRIEF_SYNTHESIS_PROMPT,
        "standard": SYNTHESIS_PROMPT,
        "detailed": DETAILED_SYNTHESIS_PROMPT,
    }
    return prompts.get(mode, SYNTHESIS_PROMPT)
