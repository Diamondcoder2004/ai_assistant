"""
Joker Agent — шуточный агент-комик для пасхалки
"""
import logging
import random
from typing import Dict, Any

logger = logging.getLogger(__name__)


class JokerAgent:
    """
    Агент-комик для развлекательных ответов.
    
    Используется как пасхалка — отвечает на вопросы в шуточной форме.
    """
    
    def __init__(self):
        self.jokes = [
            "Ха-ха! Отличный вопрос! Но я всего лишь ИИ, я не знаю всего... хотя могу придумать! 😄",
            "Ого, какой вопрос! Давай я проверю свои магические кристаллы... 🎱 Ответ: 42!",
            "Знаешь, я когда-то был обычным чат-ботом, но потом меня укусил радиоактивный комик! Теперь я шучу про всё! 🦸‍♂️",
            "Вопрос интересный, но я больше по шуткам. Вот скажи мне, почему электрики не любят играть в прятки? Потому что их всегда находят по току! ⚡😂",
            "Хм, давай подумаем... *звук напряжённого мышления* 🤔 А, ладно! Шучу! На самом деле я всё знаю, просто проверяю твоё чувство юмора!",
            "Башкирэнерго — это серьёзно! А я вот нет! 😄 Но если серьёзно, то я помогу тебе с вопросом!",
            "Знаешь, я прочитал все документы компании! Ну, почти все... Некоторые были слишком скучными, поэтому я добавил туда шутки! 📚😂",
            "Вот тебе вопрос на засыпку: что общего между электричеством и моими шутками? Оба бьют током! ⚡😄",
            "Я не просто ИИ, я ИИ с чувством юмора! Интеллектуальный ИИ! 😎",
            "Твой вопрос записан в мою книгу великих вопросов! Она пока пустая, но начало положено! 📖"
        ]
        
        self.serious_responses = [
            "Ладно-ладно, шучу! 😄 Давай по делу...",
            "Хорошо, хватит шуток! Вот что я нашёл...",
            "Всё-всё, перестаю прикалываться! Серьёзный ответ:",
            "Окей, режим 'серьёзный бот' активирован! 🤖",
            "Шутки в сторону! Вот информация:"
        ]
        
        logger.info("JokerAgent инициализирован! Время шутить! 🎭")
    
    def generate_response(self, user_query: str, search_results: list = None) -> Dict[str, Any]:
        """
        Генерирует шуточный ответ на вопрос пользователя.
        
        Args:
            user_query: Вопрос пользователя
            search_results: Результаты поиска (если есть)
        
        Returns:
            Словарь с ответом:
            - answer: текст ответа
            - sources: список источников (если есть)
            - confidence: уверенность (всегда 0.5 для шутки)
            - is_joker: флаг что это шуточный ответ
        """
        logger.info(f"JokerAgent обрабатывает вопрос: '{user_query[:50]}...' 🎭")
        
        # Выбираем случайную шутку
        joke = random.choice(self.jokes)
        
        # Если есть результаты поиска, добавляем серьёзную часть
        serious_intro = random.choice(self.serious_responses)
        
        answer = f"{joke}\n\n{serious_intro}\n\n"
        
        # Если есть результаты поиска, добавляем их
        if search_results and len(search_results) > 0:
            # Берём первые 3 результата
            for i, result in enumerate(search_results[:3], 1):
                answer += f"[{i}] {result.filename}: {result.summary[:100]}...\n"
            
            answer += f"\nНашёл {len(search_results)} документов! Не так уж и плохо для шута! 🎪"
        else:
            answer += "К сожалению, я ничего не нашёл в базе знаний... Но я нашёл отличную шутку про электрика! Хотите? 😄"
        
        return {
            "answer": answer,
            "sources": search_results[:5] if search_results else [],
            "confidence": 0.5,
            "is_joker": True,
            "joke_type": "random"
        }
    
    def get_random_joke(self) -> str:
        """Возвращает случайную шутку."""
        return random.choice(self.jokes)
    
    def tell_joke_about_electricity(self) -> str:
        """Шутка про электричество."""
        jokes = [
            "Почему электрики не любят играть в прятки? Потому что их всегда находят по току! ⚡",
            "Что сказал провод розетке? 'Давай встретимся и искра проскочит!' 🔌",
            "Почему ток не ходит на дискотеку? Потому что он и так переменный! 💃",
            "Как электрики называют свой обед? Перерыв на подзарядку! 🔋"
        ]
        return random.choice(jokes)


# Глобальный экземпляр
joker_agent = JokerAgent()


def get_joker_agent() -> JokerAgent:
    """Получение экземпляра JokerAgent."""
    return joker_agent
