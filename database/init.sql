-- =============================================================================
-- Wiki Concepts Table — LLM Wiki (Karpathy-style knowledge graph)
-- 
-- Слой глобального контекста для Agentic RAG.
-- Хранит высокоуровневые бизнес-концепции Башкирэнерго:
-- ТПП, ЛК, ДУ, тарифы, категории клиентов, процессы.
-- 
-- Используется Wiki Router перед генерацией поисковых запросов.
-- =============================================================================

CREATE TABLE IF NOT EXISTS wiki_concepts (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    concept_name TEXT NOT NULL UNIQUE,          -- "ТПП для ФЛ до 15 кВт"
    category TEXT NOT NULL,                     -- ЛК / ДУ / ТПП
    description TEXT NOT NULL,                  -- Краткая суть процесса (2-5 предложений)
    keywords TEXT[] DEFAULT '{}',               -- ["технологическое присоединение", "ФЛ", "до 15 кВт"]
    rules TEXT[] DEFAULT '{}',                  -- Ключевые бизнес-правила ["Льготный тариф раз в 3 года", ...]
    target_documents TEXT[] DEFAULT '{}',       -- Файлы-источники ["Постановление 861", "Паспорт услуги ТП до 15 кВт"]
    related_concepts TEXT[] DEFAULT '{}',       -- Связанные концепции ["ЛК", "Прибор учёта"]
    search_hints TEXT[] DEFAULT '{}',           -- Подсказки для Search Agent ["искать в паспортах услуг", "фильтр: ТПП"]
    client_types TEXT[] DEFAULT '{}',           -- Применимые типы клиентов: ФЛ, ИП, ЮЛ, any
    power_ranges TEXT[] DEFAULT '{}',           -- Применимые диапазоны мощности: <15kW, 15-150kW, ...
    source_origin TEXT DEFAULT 'manual',        -- Как создана: 'llm_extracted', 'manual', 'hybrid'
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- =============================================================================
-- Индексы для быстрого поиска
-- =============================================================================

CREATE INDEX IF NOT EXISTS idx_wiki_category ON wiki_concepts(category);
CREATE INDEX IF NOT EXISTS idx_wiki_source ON wiki_concepts(source_origin);
CREATE INDEX IF NOT EXISTS idx_wiki_created ON wiki_concepts(created_at DESC);

-- GIN индекс для поиска по массивам (keywords, related_concepts)
CREATE INDEX IF NOT EXISTS idx_wiki_keywords ON wiki_concepts USING GIN(keywords);

-- =============================================================================
-- Триггер для автообновления updated_at
-- =============================================================================

CREATE OR REPLACE FUNCTION update_wiki_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trigger_wiki_updated_at ON wiki_concepts;
CREATE TRIGGER trigger_wiki_updated_at
    BEFORE UPDATE ON wiki_concepts
    FOR EACH ROW
    EXECUTE FUNCTION update_wiki_updated_at();

-- =============================================================================
-- Включаем RLS (Row Level Security) — только сервисная роль может писать
-- =============================================================================

ALTER TABLE wiki_concepts ENABLE ROW LEVEL SECURITY;

-- Сервисная роль: полный доступ
CREATE POLICY wiki_service_policy ON wiki_concepts
    FOR ALL
    TO service_role
    USING (true)
    WITH CHECK (true);

-- Анонимные/аутентифицированные пользователи: только чтение
CREATE POLICY wiki_read_policy ON wiki_concepts
    FOR SELECT
    TO anon, authenticated
    USING (true);

-- =============================================================================
-- Таблица для логов извлечения концепций (wiki_extractor)
-- =============================================================================

CREATE TABLE IF NOT EXISTS wiki_extraction_logs (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    source_file TEXT NOT NULL,                  -- Исходный файл (из enriched_chunks)
    chunks_processed INTEGER DEFAULT 0,         -- Сколько чанков обработано
    concepts_extracted INTEGER DEFAULT 0,       -- Сколько концепций извлечено
    llm_model TEXT,                             -- Какая модель использовалась
    tokens_used INTEGER DEFAULT 0,              -- Потрачено токенов
    status TEXT DEFAULT 'pending',              -- pending / processing / completed / failed
    error_message TEXT,                         -- Ошибка если failed
    created_at TIMESTAMPTZ DEFAULT NOW()
);

ALTER TABLE wiki_extraction_logs ENABLE ROW LEVEL SECURITY;

CREATE POLICY extraction_logs_service_policy ON wiki_extraction_logs
    FOR ALL TO service_role USING (true) WITH CHECK (true);

CREATE POLICY extraction_logs_read_policy ON wiki_extraction_logs
    FOR SELECT TO anon, authenticated USING (true);

